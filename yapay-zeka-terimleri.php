  <h2>LLM (Large Language Model / Büyük Dil Modeli)</h2>
  <p class="paragraph serif">BBüyük dil modeli, inanılmaz büyüklükteki metinlerden oluşturulan bir veri setiyle eğitilmiş, insan dilini anlayabilen ve bu dilde bilgi üretebilen yapay zeka modelleridir. Günlük hayatta kullandığımız ChatGPT, Gemini, Claude, LLaMA gibi yapay zeka modelleri birer büyük dil modelidir.</p>

  <h2>Text-to-Image Models (Metinden-Görsele Modeller)</h2>
  <p class="paragraph serif">Kullanıcının metin olarak verdiği talimatları (prompt) işleyerek fotoğraflar üreten yapay zeka modelleridir. MidJourney, DALL·E, Imagen gibi modellere "Sevimli bir robot kedi" şeklinde bir prompt yazıp görsel çıktısı aldığımızda, bu modelleri kullanmış oluruz.</p>

  <h3>Veri Setleri ve Eğitim Verileri</h3>
  <p class="paragraph serif">Veri setleri, yapay zekaları eğitmek için kullanılan ve içlerinde dev eğitim verileri bulunan (kitaplar, web siteleri vb.) dosyalardır.</p>

  <h2>Fine-Tuning (İnce Ayar)</h2>
  <p class="paragraph serif">Fine-Tuning, var olan bir modelin belirli ihtiyaçlar ve konular üzerine adapte edilmesi ve yeniden eğitilmesidir. Örneğin, standart haliyle hukuki konular hakkında net bilgilere sahip olmayan bir yapay zeka, hukuk verileriyle fine-tune edilerek bu konularda uzmanlaşabilir ve hukuki belgeler üretebilir.</p>

  <h2>Sinir Ağı (Neural Network)</h2>
  <p class="paragraph serif">Sinir ağları, yapay zekanın örüntüleri öğrenmesini sağlayan bir sistemdir. Geliştirilmesinde insan beyni örnek alınmıştır. Verilerden öğrenirken insan beyninin esnekliğini taklit eder. Ayrıca bu sistem çok katmanlı olarak da kullanılabilir. Bu çok katmanlı sinir ağlarından oluşan sistemlere ise deep learning (derin öğrenme) denir</p>

  <h2>Parametreler</h2>
  <p class="paragraph serif">Parametreler, bir sinir ağının öğrenme sürecinde ayarlanabilen ve modelin davranışını belirleyen matematiksel değişkenlerdir. En basit anlatımıyla, bir model geliştirilirken ne kadar çok parametre kullanılırsa modelin kapasitesi o kadar artar. Ancak bu süreçte dengeye dikkat edilmezse model, bilgileri öğrenmek yerine sadece ezberlemeye başlar.</p>
  <p>Popüler LLM’lerin parametre sayıları şöyle:</p>
  <ul>
    <li>Gemini → 1.5 trilyon</li>
    <li>ChatGPT-4o → 1.8 trilyon</li>
    <li>DeepSeek R1 → 671 milyar</li>
  </ul>

  <h2>Token</h2>
  <p class="paragraph serif">Token’lar, yapay zeka modellerinin metni işlerken kullandığı en küçük birimlerdir. Farklı modellerde farklı şekilde kullanılabilir. Bazı yapay zekalarda token’lar harf bazında, bazılarında kelime bazında, bazılarında ise verilen metnin anlamlı parçalara bölünmesiyle nitelendirilir.</br>
  Örneğin, genel kullanımda 4 karakter 1 token olarak değerlendirilir. Ortalama bir Türkçe kelimenin 1.3 token olduğu varsayılır.</p>

  <h2>Bağlam Penceresi (Context Window)</h2>
  <p class="paragraph serif">Bağlam penceresi, bir dil modelinin tek seferde işleyebildiği maksimum token sayısını ifade eder. Bir pencerede, dil modelinin anlık olarak görebildiği ve dikkate alabildiği metin, talimat veya veri miktarı belirlenir.</br>
  Modeller bu sınıra göre kullanıcıdan veri alabilir. Örneğin, Claude 3 128k token alabilir. Bu da neredeyse tek bir istemde 300 sayfalık bir romanı analiz edebileceği anlamına gelir.
  </p>


  <h2>Prompt (İstem)</h2>
  <p class="paragraph serif">Prompt, yapay zeka modellerine yönelik talimat, soru veya yönlendirmedir. Modelin çıktısını doğrudan etkiler. Örneğin, bir yapay zekaya "Mars’a yolculuk nasıl yapılır?" şeklinde yöneltilen bir soru bir prompt’tur.
  </p>
  <h2>Prompt Mühendisliği</h2>
  <p class="paragraph serif">Prompt mühendisliği, yapay zeka modellerinden daha doğru/verimli çıktılar almak için prompt’ları stratejik olarak tasarlama sanatıdır. Prompt’ları rol verme, format belirtme gibi tekniklerle optimize ederek daha verimli çıktılar alınması hedeflenir.
  </p>
  <h2>Temperature (Sıcaklık Parametresi)</h2>
  <p class="paragraph serif">Temperature, yapay zeka modellerindeki çıktıların gerçekçilik ve yaratıcılık seviyesini kontrol eden kritik bir parametredir. 0 ile 1 arasında değer alabilir.
  </p>
  <ul>
    <li>Bilimsel bir makale için: 0.1 – 0.3</li>
    <li>Gerçekçi sohbetler için: 0.3 – 0.5</li>
    <li>Yaratıcı senaryolar için: 0.7 – 1.0</li>
  </ul>
  <p>Daha yüksek değerler anlamsız çıktılara sebep olabilir. Not: ChatGPT standart versiyonunda 0.7 sıcaklık parametresi kullanır.</p>

  <h2>Zero-Shot Learning (Sıfır Atışlı Öğrenme)</h2>
  <p class="paragraph serif">Zero-shot learning, yapay zeka modellerinin daha önce hiç eğitim almadığı bir görevi örnek görmeden gerçekleştirebilme yeteneğidir. Bu durum, yapay zeka modelinin genelleme ve bağlam anlama becerilerinin gücünü gösterir.</br>
  Örneğin, gelişmiş bir yapay zeka, sıfır atışlı öğrenme sayesinde şiir yazma, kod oluşturma ve hatta matematik ispatları yapma gibi görevleri öncesinde örnek görmediği halde gerçekleştirebilir.
  </p>



